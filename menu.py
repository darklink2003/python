def imprimir_color(color, mensaje):
    """
    Función que imprime un mensaje en un color determinado.
    @param: color     Color del mensaje.
    @param: mensaje      Mensaje a imprimir.
    """
    if color == "rojo":
        color = "\x1b[1;31m"
    elif color == "amarillo":
        color = "\x1b[1;33m"
    else:
        color = ""
    print(color + mensaje + "\x1b[0m")


