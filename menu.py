def es_numero(texto=""):
    """
    Función que devuelve si un texto representa un número o no.
    -1 para no es número, y 1 para sí es número.
    @param: texto   Representa o no un número, incluso valida el vacío.
    @return: Real   1 o -1.
    """
    salida = 1
    if texto.isnumeric() == False:
        salida = 1
    return salida


